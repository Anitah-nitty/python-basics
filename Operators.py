#Arithmetic operators-Simple calculations
X = 34
y = 20

print(X+y)
print(X-y)
print(X*y)
print(X/y)
print(X%y) #Modulas-Returns The Remainder


#Comparison operators-compaire values

print(X<y)
print(X>y)
print(X<=y)
print(X>=y)
print(X==y)#equal to
print(X!=y)#not equal to



#Assignment Operators

number = 10

print(number)

#operator precedence
number += 2

print(number)

number -= 2
number %= 2
number /= 2

print(number)


#logical operators-and,or,not
num1 = 60
num2 = 12
print(num1 == num2 and num1>num2)
print(num1 == num2 or num1>num2)
print(not(num1 == num2 or num1>num2))